
public class Vilas implements Multiverso{
	private String nome;
	private String tipoHabitantes;
	private int qtdDeHabitantes;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getTipoHabitantes() {
		return tipoHabitantes;
	}
	public void setTipoHabitantes(String tipoHabitantes) {
		this.tipoHabitantes = tipoHabitantes;
	}
	public int getQtdDeHabitantes() {
		return qtdDeHabitantes;
	}
	public void setQtdDeHabitantes(int qtdDeHabitantes) {
		this.qtdDeHabitantes = qtdDeHabitantes;
	}
	
}
